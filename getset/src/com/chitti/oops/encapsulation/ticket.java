package com.chitti.oops.encapsulation;
 
public class ticket {

	private String name;
	private String from;
	private String to;
	private short date;
	private int amount;
	private String gender;
	private byte tnumber;
	private byte age;
	private String serNo;
	private boolean sleeper;
	
	public ticket()
	{
	}
	public String getname()
	{
	return name;
	}
	public void setname(String Name)
	{
		name=Name;
	}
	
	public String getfrom()
	{
		return from;
	}
	public void setfrom(String From)
	{
		from=From;
	}
	public String getto()
	{
		return to;
	}
	public void setto(String To)
	{
		to=To;
	}
	public short getdate()
	{
		return date;
	}
	public void setdate(short Date)
	{
		date=Date;
	}
	public int getamount()
	{
		return amount;
	}
	public void setamount(int Amount )
	{
		amount=Amount;
	}
	public String getgender()
	{
		return gender;
		
	}
	public void setgender(String Gender)
	{
		gender=Gender;
	}
	public byte gettnumber()
	{
	return tnumber;	
	}
	public void settnumber(byte Tnumber)
	{
		tnumber=Tnumber;
	}
	public int getage()
	{
		return age;
	}
	public void setage(byte Age)
	{
		age= Age;
	}
	public String getserNo()
	{
		return serNo;
	}
	public void setserNo(String Serno )
	{
		serNo=Serno;
	}
	
	public boolean getsleeper()
	{
		return sleeper;
	}
	public void setsleeper(boolean Sleeper)
	{
		sleeper=Sleeper;
	}
	}
	
	
	

